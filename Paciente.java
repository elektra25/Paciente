class Paciente {
    int nss; // Variable para almacenar el NSS del paciente
    String nombre; // Variable para almacenar el nombre completo del paciente

    // Método para mostrar el NSS del paciente
    public void mostrarNss(){
        System.out.println("Mi NSS es: " + this.nss); // Imprime el NSS del paciente
    }

    // Método para mostrar el nombre completo del paciente
    public void mostrarNombre(){
        System.out.println("Mi nombre completo es: " + this.nombre); // Imprime el nombre completo del paciente
    }
}


